# Number - Integer, Float
age = 30                
gpa = 3.5
temperature = -5
# puts age.class, gpa.class, temperature.class


# Boolean - TrueClass, FalseClass
is_active = true
live_in_us = false
# puts is_active.class, live_in_us.class


# Array
numbers = [1, 5, 6, 19]
heroes = ['Superman', 'Spiderman', 'Batman', 'Ironman']
mixed = ['Hello', 10, false]
# puts numbers.class, heroes.class, mixed.class


# Hash
person_1 = {
    'name' => 'Anna',
    'age' => 30
}
person_2 = {
    name: 'Joni',
    age: 32
}
# puts person_1.class, person_2.class


# Nil - NilClass
data = nil
# puts data.class


# Range
